package com.kh618.entmaa.MyClasses;

public class WhoUsItem {

    private String termName;
    private String term;

    public WhoUsItem(String termName, String term) {
        this.termName = termName;
        this.term = term;
    }

    public String getTermName() {
        return termName;
    }

    public String getTerm() {
        return term;
    }
}
